        window = UIWindow(frame: scene.coordinateSpace.bounds)
        window?.windowScene = scene
        let tabBarController = UITabBarController()
        let firstView: HomeView = HomeView()
        let secondView: RankingView = RankingView()
        tabBarController.viewControllers = [firstView,secondView]
        
        let firstItem = UITabBarItem(title: "Home", image: .checkmark, tag: 0)
        let secondItem = UITabBarItem(title: "Ranking", image: .actions, tag: 1)
        
        firstView.tabBarItem = firstItem
        secondView.tabBarItem = secondItem
        
        let navigationController = UINavigationController(rootViewController: tabBarController)
        
        window?.rootViewController = navigationController
        window?.makeKeyAndVisible()
    }

